public class SolveOperations {
    public static void main(String[] args) {
        int A = 5, B = 6, C = 7, D = 8;
        System.out.println("(A * B - C * D) = " + (A * B - C * D));
        System.out.println("(2A - B + 3D) = " + (2 * A - B + 3 * D));
        System.out.println("(A^2 + B^2 - C^2 + D^2) = " + (A * A + B * B - C * C + D * D));
        System.out.println("(A^3 + B - C^2) = " + (A * A * A + B - C * C));
    }
}